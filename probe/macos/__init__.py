# probe/macos — macOS 屏幕捕获探针包
# =====================================================
# 对应 PRD 第三章：屏幕捕获技术方案 (探针设计) — macOS 路径
#
# 技术路线（双轨制）：
#
#   ┌─────────────────────────────────────────────────┐
#   │  首选：Accessibility API                         │
#   │  模块：accessibility_probe.py                    │
#   │  原理：通过 macOS AXUIElement API 直接读取        │
#   │        微信输入框的文本控件内容                    │
#   │  优点：精准、低延迟、无需截图                      │
#   │  要求：用户需在「系统设置 → 隐私与安全 →           │
#   │        辅助功能」中授权本软件                      │
#   └─────────────────────────────────────────────────┘
#
#   ┌─────────────────────────────────────────────────┐
#   │  备选：Vision Framework OCR                      │
#   │  模块：vision_ocr_probe.py                       │
#   │  原理：截取微信窗口区域截图，使用 Apple Vision     │
#   │        Framework 进行硬件加速 OCR 文字识别         │
#   │  适用：Accessibility API 授权被拒或失效时的降级方案 │
#   │  要求：macOS 10.15+，Apple Silicon 推荐           │
#   └─────────────────────────────────────────────────┘
#
# 核心原则（来自 PRD）：
#   - 只读不写：绝不修改微信内存、UI 控件或任何系统资源
#   - 非侵入式：仅通过官方系统 API 旁观，不侵入协议层
#   - 最小权限：仅请求辅助功能权限，不申请屏幕录制权限（OCR 模式除外）
#
# 对外接口：
#   本包向上层（probe/__init__.py）暴露统一的 MacOSProbe 入口类，
#   由 MacOSProbe 根据授权状态自动选择首选或备选探针。
#
# 子模块：
#   accessibility_probe  — Accessibility API 文本读取探针（首选）
#   vision_ocr_probe     — Vision Framework OCR 截图探针（备选）
