# ui/sidebar — 侧边栏主界面包 (Sidebar UI Package)
#
# 对应 PRD 第四章 — UI 文本框架布局 (Text Framework)
# 对应 PRD 2.6    — 智能辅助补全交互 (Ghost Text & Tab)
#
# 职责：
#   负责渲染挂载于微信窗口侧边的极客风格分栏界面。
#   本包是用户唯一可见的交互入口，所有 AI 建议在此处呈现。
#
# 子模块：
#   main_panel.py  — 侧边栏主面板：整体布局、候选建议列表、联系人信息展示
#   ghost_text.py  — 幽灵文本渲染：浅灰色预测内容叠加层 + Tab 键采纳逻辑
#
# 界面布局草图：
#   ┌─────────────────────────────┐
#   │  Social Copilot  [设置⚙️]   │  ← 标题栏
#   ├─────────────────────────────┤
#   │  👤 张总  [商务 🔵]          │  ← 当前联系人 + 场景标签
#   │  语气：[专业▾]              │  ← 语气档位选择器
#   ├─────────────────────────────┤
#   │  💡 建议 A                  │  ← 候选回复 (可点击采纳)
#   │  关于方案对接，建议本周...    │
#   │                             │
#   │  💡 建议 B                  │
#   │  收到，我这边今天内完成...    │
#   ├─────────────────────────────┤
#   │  ⌨️  正在输入：我觉得...     │  ← Ghost Text 预览区
#   │     我觉得[这个方案可行，    │  ← 浅灰色补全内容
#   │     建议本周确认]           │
#   │            [Tab: 采纳]      │
#   └─────────────────────────────┘
#
# 技术选型：
#   - PyQt6：实现跨平台桌面侧边栏窗口（无边框、透明背景、置顶）
#   - 窗口类型：Tool Window（不出现在 Dock/任务栏，跟随微信主窗口定位）
#
# 与其他层的依赖关系：
#   probe/        — 提供当前输入框文字 + 对话上下文（上游数据源）
#   brain/        — 提供 AI 生成的候选建议文本（上游数据源）
#   core/tone/    — 提供当前语气档位状态（双向绑定）
#   ui/settings/  — 设置面板（通过齿轮按钮触发弹出）
